public class RetoI {
    public static void main(String[] args) {
        //declarar variables
        int gradosF=350;
        double gradosC=0;

        System.out.println("Hola abuelita, aquí puedes convertir los 350 grados Fahrenheit a Centígrados.");

        //convertir grados Fahrenheit a centígrados
        gradosC = (gradosF-32)/1.8;

        //mostrar conversión
        System.out.println("Abuelita, los 350 grados Fahrenheit son equivalentes a " + gradosC + " grados Centígrados");

        System.out.println("Ya puedes hacer tu torta, hasta luego.");
    
    }
}
